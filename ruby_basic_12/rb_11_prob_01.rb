# Create a hash that contains the following data and assign it to a variable named car.

# type	color	mileage
# sedan	blue	80_000

# ANSWER:

# car = {
#   :type => "sedan",
#   :color => "blue",
#   :milage => 80_000
# }

car = {
  type: "sedan",
  color: "blue",
  milage: 80_000
}

p car

# DISCUSSION:

# We can create a hash two ways, using the old syntax by assigning values to a symbol with a => or simply using the
# has = {key: "value"} format.

